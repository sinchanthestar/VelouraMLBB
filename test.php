function generateTemplate($payload): string
{
    $tmp = "(";
    $payload = preg_replace("/'|\"|;/","",$payload);
    for ($i=0;$i<strlen($payload);$i++){
        if ($payload[$i]=="("){
            $tmp.=")(";
        }elseif ($payload[$i]==")"){
            $tmp.=")";
        }else{
            if ($payload[$i+1]==")"||$payload[$i+1]=="("){
                $tmp.="("."chr(".generateNum(ord($payload[$i])).")".")";
            }else{
                $tmp.="("."chr(".generateNum(ord($payload[$i])).")".").";
            }
        }
    }
    return $tmp;
}

